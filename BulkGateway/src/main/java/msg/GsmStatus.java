package msg;

public class GsmStatus {
    private int status;

    public GsmStatus(int status) {
        this.status = status;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getStatusValue() {
        return status;
    }

    public int getReasonCode() {
        return status;
    }
}
