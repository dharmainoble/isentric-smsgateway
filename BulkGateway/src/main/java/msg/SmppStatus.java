package msg;

public class SmppStatus {
    private int status;

    public SmppStatus(int status) {
        this.status = status;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getMessageState() {
        return status;
    }

    public int getErrorCode() {
        return status;
    }
}
